void main() {
  ///integer type variable
  int a = 10;
  print(a);

  ///string type variable
  String s = "Dart with Umer";
  print(s);

  ///double type variable
  double d = 2.53;
  print(d);

  ///list type variable
  List l = [1, 2, 3, 4, 'hello world', 2.1];
  print(l);
  print(l[3]);

  /// map type variable
  Map m = {'name': 'Umer', 'city': 'Lahore'};
  print(m);
  print(m['city']);

  ///Dynamic type variable
  var v = 'Ali';
  var v2 = 10;
  var v3 = [10, 20, 30];
  print(v);
  print(v2);
  print(v3);
}
